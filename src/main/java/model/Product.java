package model;

public class Product {
    String productCode;
    int productType;
    String productName;
    int productPrice;
    String productUnit;
    String productCountry;
    String productSpecies;
    String productCompany;
    int productTier;
    String productImage;
    int productVisible;

    public String getProductCode() {
        return productCode;
    }

    public void setProductCode(String productCode) {
        this.productCode = productCode;
    }

    public int getProductType() {
        return productType;
    }

    public void setProductType(int productType) {
        this.productType = productType;
    }

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public int getProductPrice() {
        return productPrice;
    }

    public void setProductPrice(int productPrice) {
        this.productPrice = productPrice;
    }

    public String getProductUnit() {
        return productUnit;
    }

    public void setProductUnit(String productUnit) {
        this.productUnit = productUnit;
    }

    public String getProductCountry() {
        return productCountry;
    }

    public void setProductCountry(String productCountry) {
        this.productCountry = productCountry;
    }

    public String getProductSpecies() {
        return productSpecies;
    }

    public void setProductSpecies(String productSpecies) {
        this.productSpecies = productSpecies;
    }

    public String getProductCompany() {
        return productCompany;
    }

    public void setProductCompany(String productCompany) {
        this.productCompany = productCompany;
    }

    public int getProductTier() {
        return productTier;
    }

    public void setProductTier(int productTier) {
        this.productTier = productTier;
    }

    public String getProductImage() {
        return productImage;
    }

    public void setProductImage(String productImage) {
        this.productImage = productImage;
    }

    public int getProductVisible() {
        return productVisible;
    }

    public void setProductVisible(int productVisible) {
        this.productVisible = productVisible;
    }
}
